    private void bt_salvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_salvarMouseClicked
        if (verificaCampos()) {

            if (mode == 1) {
               EditoraBEAN editora = new EditoraBEAN(this.razao_social, this.endereco, this.telefone, true); 
               controle.addEditora(editora);
               aux.preencher_tabela(controle.listaEditoras());
               JOptionPane.showMessageDialog(null, "Gravado!","Gravado",JOptionPane.INFORMATION_MESSAGE);
               this.dispose(); 
            }else if (mode == 2) {
               EditoraBEAN editora = new EditoraBEAN(this.id, this.razao_social, this.endereco, this.telefone, true);
               controle.updateEditora(editora);
               aux.preencher_tabela(controle.listaEditoras());
               JOptionPane.showMessageDialog(null, "Alterado!","Alterado",JOptionPane.INFORMATION_MESSAGE);
               this.dispose();
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!"
                    + "\nConfirme os valores!","ERRO",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_bt_salvarMouseClicked

