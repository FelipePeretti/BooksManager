    private boolean verificaCampos(){
        this.razao_social = this.t_razao_social.getText();
        this.endereco = this.t_endereco.getText();
        if (!this.t_telefone.getText().equals("")) {
            this.telefone = Integer.parseInt(this.t_telefone.getText());
        }else{
            this.telefone = -1;
        }
        
        if (this.razao_social != "" && this.endereco != "" && this.telefone > 90000000 && this.telefone < 900000000) {
            return true;
        } else{
            return false;
        }
    }
    
